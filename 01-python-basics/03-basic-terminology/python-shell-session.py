# Basic Terminology

# Expression
print(2 + 2)        # Returns 4
print(5 - 3)        # Returns 2
print(3 * 7)        # Returns 21
print(22 / 7)       # Returns 3.1428...
print(2 + 3 * 6)    # Returns 20
print((2 + 3) * 6)  # Returns 30
print((5 - 1) * ((7 + 1) / ( 3 - 1)))   # Returns 16.0


# Data Type
print(type(1.43))       # Data type of float
print(type("hello"))    # Data type of str
print(type(10))         # Data type of int
print(type(True))       # Data type of bool


# String Concatenation
print("Hello " + "World")


# String Replication
print("Hello World" + "!" * 10)


# Variable
spam = 42
print("spam = " + str(spam))

spam = "Hello"
print("spam = " + spam)
print(spam + " world!" )


# Statement
spam = 2 + 2    # 2 + 2' is an expression; the entire line is a statement

